@app.post("/monitoring/start", tags=["Monitoring"], summary="Starts real-time data monitoring")
async def start_monitoring():
    global surveillance_thread
    if not all([realtime_model, realtime_scaler]):
         raise HTTPException(status_code=503, detail="Real-time model or scaler not loaded.")
    if surveillance_thread and surveillance_thread.is_alive():
        return {"status": "already_running"}
    stop_flag.clear()
    surveillance_thread = Thread(target=surveillance_loop_runner)
    surveillance_thread.start()
    return {"status": "started"}

@app.post("/monitoring/stop", tags=["Monitoring"], summary="Stops real-time data monitoring")
async def stop_monitoring():
    global surveillance_thread
    if not surveillance_thread or not surveillance_thread.is_alive():
        return {"status": "not_running"}
    stop_flag.set()
    surveillance_thread.join(timeout=5)
    surveillance_thread = None
    return {"status": "stopped"}

