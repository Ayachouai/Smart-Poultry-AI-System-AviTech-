def fetch_and_predict_realtime():
    """Fetches data, predicts, saves, and broadcasts via WebSocket."""
    try:
        token = get_token()
        if not token:
            if main_loop: asyncio.run_coroutine_threadsafe(manager.broadcast(json.dumps({"error": "Failed to get API token."})), main_loop)
            return

        headers = {"Authorization": f"Token {token}"}
        farm_code = get_farm_code(headers)
        if not farm_code:
            if main_loop: asyncio.run_coroutine_threadsafe(manager.broadcast(json.dumps({"error": "Failed to get farm code."})), main_loop)
            return

        data = fetch_farm_data(headers, farm_code)
        if not data:
            print(f"[{datetime.now()}] No new data received from API.")
            return

        df = pd.DataFrame(data)
        df_flat = pd.json_normalize(df['data'])
        df = pd.concat([df[['ts']], df_flat], axis=1)
        df['datetime'] = pd.to_datetime(df['ts'].astype(int), unit='s')
        df = df.rename(columns={'age': 'Age', 'co2': 'CO2', 't_t': 'Req Temp', 't_cur': 'Temp', 'h_r': 'Humidity'})

        features_to_check = ['Age', 'CO2', 'Req Temp', 'Temp', 'Humidity']
        for col in features_to_check:
            df[col] = pd.to_numeric(df[col], errors='coerce')
        df.dropna(subset=features_to_check, inplace=True)

        if df.empty: return

        df['Humidite_Critique (%)'] = df['Age'].apply(get_humidite_critique)
        features = ['Age', 'CO2', 'Req Temp', 'Temp', 'Humidity', 'Humidite_Critique (%)']

        X = df[features]
        X_scaled = realtime_scaler.transform(X)
        y_proba = realtime_model.predict_proba(X_scaled)[:, 1]

        df['Status'] = np.where(y_proba >= 0.5, 'Abnormal', 'Normal')
        df['Proba_Abnormal_pct'] = (y_proba * 100).round(1)
        df['Proba_Normal_pct'] = ((1 - y_proba) * 100).round(1)
        print(f"[{datetime.now()}] Real-time prediction successful. Found {len(df)} records.")

        try:
            df_for_history = df[['datetime'] + features + ['Status', 'Proba_Normal_pct', 'Proba_Abnormal_pct']]
            header = not os.path.exists(CSV_PATH)
            df_for_history.to_csv(CSV_PATH, mode='a', header=header, index=False)
        except Exception as e:
            print(f"[{datetime.now()}] ⚠️ WARNING: Could not save to history file: {e}")

        df['datetime_str'] = df['datetime'].dt.strftime('%Y-%m-%d %H:%M:%S')
        cols_for_flutter = ['datetime_str', 'Temp', 'Humidity', 'CO2', 'Status', 'Proba_Normal_pct', 'Proba_Abnormal_pct']
        for _, row in df[cols_for_flutter].iterrows():
            if main_loop:
                asyncio.run_coroutine_threadsafe(manager.broadcast(row.to_json()), main_loop)

    except Exception as e:
        error_msg = f"Critical error in surveillance loop: {str(e)}"
        print(f"[{datetime.now()}] ❌ {error_msg}")
        if main_loop:
            asyncio.run_coroutine_threadsafe(manager.broadcast(json.dumps({"error": error_msg})), main_loop)

def surveillance_loop_runner():
    print(">>> Background surveillance loop is now ACTIVE.")
    while not stop_flag.is_set():
        fetch_and_predict_realtime()
        time.sleep(15)
    print(">>> Background surveillance loop has TERMINATED.")


