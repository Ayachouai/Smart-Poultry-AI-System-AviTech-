def load_model(path, model_type="joblib"):
    if not os.path.exists(path):
        print(f"⚠️ Warning: Model file not found at {path}")
        return None
    try:
        if model_type == "joblib": model = joblib.load(path)
        elif model_type == "tflite":
            interpreter = tf.lite.Interpreter(model_path=path)
            interpreter.allocate_tensors()
            return interpreter
        elif model_type == "h5": model = tf.keras.models.load_model(path)
        else: return None
        print(f"✅ {model_type.capitalize()} model loaded: {path}")
        return model
    except Exception as e:
        print(f"❌ Error loading {path}: {e}")
        return None

